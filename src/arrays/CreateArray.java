package arrays;

public class CreateArray {

    public static void main(String[] args) {

        int numbers[] = {34, 59, 19};

        int first = numbers[0],
                middle = numbers[1],
                last = numbers[2];

        System.out.println("First element: " + first);
        System.out.println("Middle element: " + middle);
        System.out.println("Last element: " + last);


    }
}

